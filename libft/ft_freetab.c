#include "libft.h"

void	ft_freetab(char ***tab)
{
	long	i;

	if (*tab && **tab)
	{
		i = 0;
		while ((*tab)[i] != NULL)
		{
			free((*tab)[i]);
			++i;
		}
		free(*tab);
		*tab = NULL;
	}
}
