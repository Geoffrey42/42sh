#include "libft.h"

void	ft_put_array(char **array, char *c)
{
	int	i;

	i = 0;
	if (!array || !c)
		return ;
	while (array[i])
	{
		ft_putstr(array[i]);
		ft_putstr(c);
		i++;
	}
}
