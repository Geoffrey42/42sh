#include <unistd.h>

void	ft_putchar(unsigned char cp)
{
	unsigned int temp;

	if (cp < 128)
		write(1, &cp, 1);
	else
	{
		temp = (192 | (cp >> 6));
		write(1, &temp, 1);
		temp = (128 | (cp & 63));
		write(1, &temp, 1);
	}
}
